import mongoose from 'mongoose';
//schema
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
); // two fields that gives timstamps (createdApp and UpdatedApp)

export const User = mongoose.model('User', userSchema);
